package libgdx.ui.model.inventory;

public class UserInventory {

    private int id;
    private int userId;
    private int coins;
    private int diamond;
    private int experience;

    public int getId() {
        return id;
    }

    public int getUserId() {
        return userId;
    }

    public int getCoins() {
        return coins;
    }

    public int getDiamond() {
        return diamond;
    }

    public int getExperience() {
        return experience;
    }
}
